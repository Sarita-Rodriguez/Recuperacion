    private ArrayList<Libro> ListarVehiculos = new ArrayList<>();
    private DefaultTableModel modeloVehiculos = new DefaultTableModel();

    public void setModeloVehiculos() {
        String[] cabecera = {"Nro.", "TITULO", "AUTOR", "ISBN", "PAGINAS", "EDICION", "EDITORIAL", "CIUDAD", "PAIS", "FECHA DE EDICION"};
    modeloVehiculos.setColumnIdentifiers(cabecera);

    LibroControlador libroControlador = new LibroControlador();
    ArrayList<Object[]> datos = libroControlador.datosLibrosOrdenadosPorTitulo();

    if (datos != null) {
        for (Object[] fila : datos) {
            modeloVehiculos.addRow(fila);
        }
    }

    jTableLibros.setModel(modeloVehiculos);
    }

    public void mostrarDatosVehiculos(Libro libro) {
        DefaultTableModel modelo = (DefaultTableModel) jTableLibros.getModel();
        Object[] fila = new Object[10];

        fila[0] = modelo.getRowCount() + 1;
        fila[1] = libro.getTitulo();
        fila[2] = libro.getAutor();
        fila[3] = libro.getISBN();
        fila[4] = libro.getPaginas();
        fila[5] = libro.getEdicion();
        fila[6] = libro.getEditorial();
        fila[7] = libro.getCiudad();
        fila[8] = libro.getPais();
        fila[9] = libro.getFechaEdicion();
        modelo.addRow(fila);
    }

    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
   String titulo = txtTitulo.getText();
String nombreCompletoAutor = txtautor.getText();
String isbn = txtISBN.getText();
int paginas = Integer.parseInt(txtpaginas.getText());
int edicion = Integer.parseInt(txtedicion.getText());
String editorial = txteditorial.getText();
String ciudad = txtciudad.getText();
String pais = txtpais.getText();
Date fechaEdicion = java.sql.Date.valueOf(txtfechaEdicion.getText()); 

String[] partesNombre = nombreCompletoAutor.split(" ");
String nombreAutor = partesNombre[0]; 
String apellidoAutor = partesNombre.length > 1 ? partesNombre[1] : ""; 

Persona autor = new Persona();
autor.setNombre(nombreAutor);
autor.setApellido(apellidoAutor);
Libro libro = new Libro(titulo, autor, isbn, paginas, edicion, editorial, ciudad, pais, fechaEdicion);
LibroControlador lc = new LibroControlador();
lc.agregarLibro(libro);
        
    }//GEN-LAST:event_btnInsertarActionPerformed

